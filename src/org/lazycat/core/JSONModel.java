package org.lazycat.core;
/*
 * JSONModel is a parent class of the JSON origin model of order form.
 * */

public class JSONModel {
	
	private String head;
	private String body;
	
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	
	
	
}
